  generate: {
    async routes () {
      const {data} = await axios.get('https://public-api.wordpress.com/rest/v1.1/sites/nuxtjsblog.wordpress.com/posts')
      const routes = data.posts.map((post)=>{
        return `/posts/${post.ID}`
      })
      routes.push("/categories/物語")
      return routes
    }
  }
